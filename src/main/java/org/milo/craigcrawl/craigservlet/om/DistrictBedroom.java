package org.milo.craigcrawl.craigservlet.om;


public class DistrictBedroom
{
	private int bedroom;

	private Double northwest;

	private Double northeast;
	
	private Double central;
	
	private Double southwest;
	
	private Double south;
	
	private Double southeast;
	
	private Double average;

	/**
	 * @return the bedroom
	 */
	public int getBedroom()
	{
		return bedroom;
	}

	/**
	 * @param bedroom
	 *            the bedroom to set
	 */
	public void setBedroom(int bedroom)
	{
		this.bedroom = bedroom;
	}

	/**
	 * @return the northwest
	 */
	public Double getNorthwest()
	{
		return northwest;
	}

	/**
	 * @param northwest the northwest to set
	 */
	public void setNorthwest(Double northwest)
	{
		this.northwest = northwest;
	}

	/**
	 * @return the northeast
	 */
	public Double getNortheast()
	{
		return northeast;
	}

	/**
	 * @param northeast the northeast to set
	 */
	public void setNortheast(Double northeast)
	{
		this.northeast = northeast;
	}

	/**
	 * @return the central
	 */
	public Double getCentral()
	{
		return central;
	}

	/**
	 * @param central the central to set
	 */
	public void setCentral(Double central)
	{
		this.central = central;
	}

	/**
	 * @return the southwest
	 */
	public Double getSouthwest()
	{
		return southwest;
	}

	/**
	 * @param southwest the southwest to set
	 */
	public void setSouthwest(Double southwest)
	{
		this.southwest = southwest;
	}

	/**
	 * @return the south
	 */
	public Double getSouth()
	{
		return south;
	}

	/**
	 * @param south the south to set
	 */
	public void setSouth(Double south)
	{
		this.south = south;
	}

	/**
	 * @return the southeast
	 */
	public Double getSoutheast()
	{
		return southeast;
	}

	/**
	 * @param southeast the southeast to set
	 */
	public void setSoutheast(Double southeast)
	{
		this.southeast = southeast;
	}

	/**
	 * @return the average
	 */
	public Double getAverage()
	{
		return average;
	}

	/**
	 * @param average the average to set
	 */
	public void setAverage(Double average)
	{
		this.average = average;
	}

}
